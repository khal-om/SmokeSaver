  let lastQuote = null;

  function getTier(amount) {
    if (amount === 0) return 'zero';
    if (amount <= THRESHOLDS.small) return 'small';
    if (amount <= THRESHOLDS.medium) return 'medium';
    return 'large';
  }

  function pick(amount) {
    const pool = POOLS[getTier(amount)];
    let quote;
    do {
      quote = pool[Math.floor(Math.random() * pool.length)];
    } while (quote === lastQuote && pool.length > 1);
    lastQuote = quote;
    return quote;
  }

